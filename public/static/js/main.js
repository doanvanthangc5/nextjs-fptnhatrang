function getQueryVariable(o) {
  for (
    var t = window.location.search.substring(1).split('&'), e = 0;
    e < t.length;
    e++
  ) {
    var a = t[e].split('=');
    if (a[0] == o) return a[1];
  }
  return !1;
}

function displayNotify() {
  for (var o in globalStorage)
    if ('1' === globalStorage[o].is_push && 1 === globalStorage[o].is_show) {
      var t = globalStorage[o];
      printTemplate(t.name, t.excerpt, t.content_url),
        (globalStorage[o].is_push = '2');
    }
}

function printTemplate(o, t, e) {
  var a =
    "<div class='box-notf'><div class='row'><div class='col-xs-4 col-sm-4'><div class='image'>" +
    ("<a href='" +
      (e = e.replace('https://fptnhatrang.vn', baseUrl)) +
      "' target='_blank'><img src='/html-v2/images/logo2-01.png'/></a>") +
    "</div></div><div class='col-xs-8 col-sm-8'><h4 class='title'>" +
    ("<a href='" + e + "' target='_blank'>" + o + '</a>') +
    "</h4><p class='excerpt'>" +
    ("<a href='" + e + "' target='_blank'>" + t + '</a>') +
    "</p></div></div><div class='close-btn'><i class='fa fa-times'></i></div></div>";
  $('.notf-area').append(a);
}

function closeNotf() {
  $('html').on('click', '.close-btn', function () {
    $(this).closest('div.box-notf').remove();
  });
}
