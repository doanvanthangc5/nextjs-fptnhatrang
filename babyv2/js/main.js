function getQueryVariable(o) {
    for (var t = window.location.search.substring(1).split("&"), e = 0; e < t.length; e++) {
        var a = t[e].split("=");
        if (a[0] == o) return a[1]
    }
    return !1
}

function displayNotify() {
    for (var o in globalStorage)
        if ("1" === globalStorage[o].is_push && 1 === globalStorage[o].is_show) {
            var t = globalStorage[o];
            printTemplate(t.name, t.excerpt, t.content_url), globalStorage[o].is_push = "2"
        }
}

